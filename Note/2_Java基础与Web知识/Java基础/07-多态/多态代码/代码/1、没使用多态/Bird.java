/**
 * 小鸟类
 */
public class Bird {
	private String name;//小鸟名字
	private String sex;//小鸟性别
	private int health;//小鸟健康值
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getHealth() {
		return health;
	}
	public void setHealth(int health) {
		this.health = health;
	}
}
